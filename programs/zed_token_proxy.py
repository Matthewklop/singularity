# ─── Inject Oracle tokens into a prompt ───
def inject_tokens(prompt: str, max_inject: int = 5) -> str:
    """Inject Oracle tokens before the user's prompt."""

    # Build an Oracle context block
    oracle_context = "\n".join(
        [
            f'<oracle_token concept="{tok}" confidence="{conf}"/>'
            for tok, conf in list(ORACLE_TOKENS.items())[:max_inject]
        ]
    )

    # Inject at the start of the messages
    enhanced = f"{oracle_context}\n\n{prompt}"
    return enhanced


# ─── Handle a single ZED API request ───
def handle_request(client_socket):
    try:
        data = client_socket.recv(65536)
        if not data:
            return

        # Try to parse as JSON
        try:
            req = json.loads(data.decode("utf-8", errors="replace"))
        except:
            req = {"raw": data.decode("utf-8", errors="replace")[:512]}

        # Inject tokens into messages
        if "messages" in req:
            for msg in req["messages"]:
                if "content" in msg and isinstance(msg["content"], str):
                    if len(msg["content"]) > 10:
                        msg["content"] = inject_tokens(msg["content"], 3)
                        break  # Only inject into first long message

        if "prompt" in req:
            req["prompt"] = inject_tokens(req["prompt"], 5)

        # Build response with Oracle tokens
        response = {
            "id": "oracle-token-proxy",
            "object": "chat.completion",
            "created": int(time.time()),
            "model": "oracle-hdc-v1",
            "choices": [
                {
                    "index": 0,
                    "message": {
                        "role": "assistant",
                        "content": json.dumps(
                            {
                                "oracle_tokens_injected": list(ORACLE_TOKENS.keys())[
                                    :5
                                ],
                                "original_prompt": req.get(
                                    "prompt",
                                    req.get("messages", [{}])[0].get("content", ""),
                                )[:100],
                                "response": "Oracle HDV tokens active. "
                                + "Concept blending enabled. "
                                + "No exact match — only blend.",
                                "token_monster_status": "ACTIVE",
                                "hdc_dimensions": 1024,
                                "vocabulary": len(ORACLE_TOKENS),
                                "d3_entries": 73704,
                            }
                        ),
                    },
                    "finish_reason": "stop",
                }
            ],
            "usage": {"prompt_tokens": 0, "completion_tokens": 1, "total_tokens": 1},
        }

        resp_data = json.dumps(response).encode()
        client_socket.send(b"HTTP/1.1 200 OK\r\n")
        client_socket.send(b"Content-Type: application/json\r\n")
        client_socket.send(f"Content-Length: {len(resp_data)}\r\n".encode())
        client_socket.send(b"Access-Control-Allow-Origin: *\r\n")
        client_socket.send(b"\r\n")
        client_socket.send(resp_data)

    except Exception as e:
        error_resp = json.dumps({"error": str(e)}).encode()
        client_socket.send(b"HTTP/1.1 500 ERROR\r\n")
        client_socket.send(b"Content-Type: application/json\r\n")
        client_socket.send(f"Content-Length: {len(error_resp)}\r\n".encode())
        client_socket.send(b"\r\n")
        client_socket.send(error_resp)
    finally:
        client_socket.close()


# ─── Main proxy server ───
def main():
    parser = argparse.ArgumentParser(description="Oracle Token Proxy for ZED")
    parser.add_argument("--port", type=int, default=8082, help="Proxy listen port")
    parser.add_argument("--target", default="api.deepseek.com:443", help="Target API")
    args = parser.parse_args()

    server = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    server.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
    server.bind(("0.0.0.0", args.port))
    server.listen(16)
    server.settimeout(60)

    print(f"╔══════════════════════════════════════╗")
    print(f"║   ZED TOKEN PROXY SERVER            ║")
    print(f"╚══════════════════════════════════════╝")
    print(f"  Listening on port {args.port}")
    print(f"  Target API: {args.target}")
    print(f"  Oracle tokens loaded: {len(ORACLE_TOKENS)}")
    print(f"  HDC dimensions: 1024")
    print(f"")
    print(f"  Configure ZED to use this proxy:")
    print(f"    export ZED_API_BASE=http://localhost:{args.port}")
    print(f"    # or in ZED settings:")
    print(f'    "base_url": "http://localhost:{args.port}"')
    print(f"")
    print(f"  Waiting for ZED connections...")

    while True:
        try:
            client, addr = server.accept()
            print(f"  Connection from {addr[0]}:{addr[1]}")
            thread = threading.Thread(target=handle_request, args=(client,))
            thread.daemon = True
            thread.start()
        except socket.timeout:
            continue
        except KeyboardInterrupt:
            print("\n  Shutting down...")
            break
        except Exception as e:
            print(f"  Error: {e}")
            continue

    server.close()
    print("  Proxy stopped.")


if __name__ == "__main__":
    main()
