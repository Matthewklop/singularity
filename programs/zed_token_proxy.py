#!/usr/bin/env python3
"""
zed_token_proxy.py — Oracle Token Injection Proxy for ZED

Intercepts ZED's API requests and injects Oracle HDV tokens
into the completion stream. This lets ZED accept and use the
hyperdimensional tokens generated by the token monster.

ZED connects to this proxy instead of directly to the API.
The proxy:
  1. Receives ZED's request
  2. Injects Oracle tokens into the context
  3. Forwards to the real API (or generates locally)
  4. Returns the enhanced completion

Run: python3 zed_token_proxy.py [--port 8082] [--target api.deepseek.com:443]
"""

